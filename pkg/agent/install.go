type InstallOptions struct {
	RootDir string
	Tools   []string
	Force   bool
}

type UpdateOptions struct {
	RootDir string
	Force   bool
}

type Result struct {
	RootDir       string
	InstallScope  string
	Tools         []string
	Files         []string
	ManifestPath  string
	CommandCount  int
	BundleVersion string
}

type Manifest struct {
	ManagedBy      string   `yaml:"managed-by"`
	SchemaVersion  int      `yaml:"schema-version"`
	BundleVersion  string   `yaml:"bundle-version"`
	InstallScope   string   `yaml:"install-scope"`
	Tools          []string `yaml:"tools"`
	Files          []string `yaml:"files"`
	CommandCount   int      `yaml:"command-count"`
	CommandCatalog string   `yaml:"command-catalog"`
}

type asset struct {
	Path    string
	Content string
}

type commandInfo struct {
	Path  string
	Short string
	Group string
}

type installTarget struct {
	Scope              string
	RootDir            string
	ManifestRelPath    string
	CommandCatalogPath string
}

func Init(root *cobra.Command, opts InstallOptions) (Result, error) {
	target, err := resolveInstallTarget(opts.RootDir)
	if err != nil {
		return Result{}, err
	}

	tools, err := normalizeTools(opts.Tools, target.Scope)
	if err != nil {
		return Result{}, err
	}

	return install(root, target, tools, opts.Force, loadManagedPaths(target))
}

func Update(root *cobra.Command, opts UpdateOptions) (Result, error) {
	target, err := resolveInstallTarget(opts.RootDir)
	if err != nil {
		return Result{}, err
	}

	manifest, err := loadManifest(target)
	if err != nil {
		return Result{}, err
	}

	tools, err := normalizeTools(manifest.Tools, target.Scope)
	if err != nil {
		return Result{}, err
	}

	return install(root, target, tools, opts.Force, pathSet(manifest.Files))
}

func SupportedTools() []string {
	return append([]string(nil), allSupportedTools...)
}

func install(root *cobra.Command, target installTarget, tools []string, force bool, managedPaths map[string]struct{}) (Result, error) {
	if root == nil {
		return Result{}, fmt.Errorf("root command is required")
	}

	commands := collectLeafCommands(root)
	assets := renderAssets(target, commands, tools)
	manifest := Manifest{
		ManagedBy:      managedBy,
		SchemaVersion:  manifestSchema,
		BundleVersion:  bundleVersion,
		InstallScope:   target.Scope,
		Tools:          append([]string(nil), tools...),
		Files:          append(assetPaths(assets), target.ManifestRelPath),
		CommandCount:   len(commands),
		CommandCatalog: target.CommandCatalogPath,
	}
	manifestAsset := asset{
		Path:    target.ManifestRelPath,
		Content: renderManifest(manifest),
	}
	nextManagedPaths := pathSet(manifest.Files)

	for _, generated := range assets {
		if err := writeManagedFile(target.RootDir, generated, force, managedPaths); err != nil {
			return Result{}, err
		}
	}
	if err := removeStaleManagedFiles(target.RootDir, managedPaths, nextManagedPaths); err != nil {
		return Result{}, err
	}
	if err := writeManagedFile(target.RootDir, manifestAsset, force, managedPaths); err != nil {
		return Result{}, err
	}

	return Result{
		RootDir:       target.RootDir,
		InstallScope:  target.Scope,
		Tools:         append([]string(nil), tools...),
		Files:         manifest.Files,
		ManifestPath:  target.ManifestRelPath,
		CommandCount:  len(commands),
		BundleVersion: bundleVersion,
	}, nil
}

func resolveInstallTarget(rootDir string) (installTarget, error) {
	if strings.TrimSpace(rootDir) == "" {
		home, err := os.UserHomeDir()
		if err != nil {
			return installTarget{}, fmt.Errorf("resolve home directory: %w", err)
		}
		if home == "" {
			return installTarget{}, fmt.Errorf("resolve home directory: empty home path")
		}
		return installTarget{
			Scope:              ScopeGlobal,
			RootDir:            home,
			ManifestRelPath:    globalManifestRelPath,
			CommandCatalogPath: globalCommandCatalogRelPath,
		}, nil
	}

	abs, err := filepath.Abs(rootDir)
	if err != nil {
		return installTarget{}, fmt.Errorf("resolve root dir: %w", err)
	}
	return installTarget{
		Scope:              ScopeLocal,
		RootDir:            abs,
		ManifestRelPath:    localManifestRelPath,
		CommandCatalogPath: localCommandCatalogRelPath,
	}, nil
}

func normalizeTools(tools []string, scope string) ([]string, error) {
	supported := supportedToolsForScope(scope)
	if len(tools) == 0 {
		return append([]string(nil), defaultToolsForScope(scope)...), nil
	}

	seen := map[string]struct{}{}
	normalized := make([]string, 0, len(tools))
	for _, tool := range tools {
		tool = strings.ToLower(strings.TrimSpace(tool))
		if tool == "" {
			continue
		}
		if !slices.Contains(supported, tool) {
			return nil, fmt.Errorf("unsupported tool %q for %s installs (supported: %s)", tool, scope, strings.Join(supported, ", "))
		}
		if _, ok := seen[tool]; ok {
			continue
		}
		seen[tool] = struct{}{}
		normalized = append(normalized, tool)
	}

	slices.SortFunc(normalized, func(a, b string) int {
		return toolOrder(a) - toolOrder(b)
	})
	return normalized, nil
}

func supportedToolsForScope(scope string) []string {
	switch scope {
	case ScopeGlobal:
		return append([]string(nil), defaultGlobalTools...)
	default:
		return append([]string(nil), defaultLocalTools...)
	}
}

func defaultToolsForScope(scope string) []string {
	switch scope {
	case ScopeGlobal:
		return defaultGlobalTools
	default:
		return defaultLocalTools
	}
}

func toolOrder(tool string) int {
	switch tool {
	case ToolGeneric:
		return 0
	case ToolCodex:
		return 1
	case ToolClaude:
		return 2
	case ToolOpenCode:
		return 3
	case ToolCopilot:
		return 4
	default:
		return 99
	}
}

func loadManifest(target installTarget) (Manifest, error) {
	path := filepath.Join(target.RootDir, filepath.FromSlash(target.ManifestRelPath))
	data, err := os.ReadFile(path)
	if err != nil {
		if os.IsNotExist(err) {
			switch target.Scope {
			case ScopeLocal:
				return Manifest{}, fmt.Errorf("agent manifest not found at %s; run `tscli agent init --dir %s` first", target.ManifestRelPath, target.RootDir)
			default:
				return Manifest{}, fmt.Errorf("agent manifest not found at %s; run `tscli agent init` first", target.ManifestRelPath)
			}
		}
		return Manifest{}, fmt.Errorf("read manifest: %w", err)
	}

	var manifest Manifest
	if err := yaml.Unmarshal(data, &manifest); err != nil {
		return Manifest{}, fmt.Errorf("decode manifest: %w", err)
	}
	if manifest.ManagedBy != managedBy {
		return Manifest{}, fmt.Errorf("manifest at %s is not managed by tscli agent", target.ManifestRelPath)
	}
	switch manifest.SchemaVersion {
	case 0, manifestSchema:
	default:
		return Manifest{}, fmt.Errorf("manifest at %s uses unsupported schema-version %d (expected %d); rerun `tscli agent init` to regenerate it", target.ManifestRelPath, manifest.SchemaVersion, manifestSchema)
	}
	if manifest.InstallScope == "" {
		manifest.InstallScope = target.Scope
	}
	if manifest.InstallScope != target.Scope {
		if manifest.InstallScope == ScopeLocal {
			return Manifest{}, fmt.Errorf("manifest at %s is for a local install; rerun with `tscli agent update --dir %s`", target.ManifestRelPath, target.RootDir)
		}
		return Manifest{}, fmt.Errorf("manifest at %s is for a global install; rerun with `tscli agent update`", target.ManifestRelPath)
	}
	return manifest, nil
}

func collectLeafCommands(root *cobra.Command) []commandInfo {
	var commands []commandInfo
	var walk func(*cobra.Command)

	walk = func(cmd *cobra.Command) {
		if cmd.Run != nil || cmd.RunE != nil {
			fullPath := strings.TrimSpace(cmd.CommandPath())
			relativePath := strings.TrimSpace(strings.TrimPrefix(fullPath, root.Name()))
			if relativePath == "" {
				relativePath = root.Name()
			}
			group := relativePath
			if idx := strings.IndexByte(relativePath, ' '); idx >= 0 {
				group = relativePath[:idx]
			}
			commands = append(commands, commandInfo{
				Path:  fullPath,
				Short: strings.TrimSpace(cmd.Short),
				Group: group,
			})
		}

		children := append([]*cobra.Command(nil), cmd.Commands()...)
		slices.SortFunc(children, func(a, b *cobra.Command) int {
			return strings.Compare(a.Name(), b.Name())
		})
		for _, child := range children {
			if shouldExcludeCommand(child) {
				continue
			}
			walk(child)
		}
	}

	walk(root)
	slices.SortFunc(commands, func(a, b commandInfo) int {
		if groupCmp := strings.Compare(a.Group, b.Group); groupCmp != 0 {
			return groupCmp
		}
		return strings.Compare(a.Path, b.Path)
	})
	return commands
}

func shouldExcludeCommand(cmd *cobra.Command) bool {
	if cmd.Hidden {
		return true
	}
	switch cmd.Name() {
	case "help", "completion":
		return true
	default:
		return false
	}
}

func renderAssets(target installTarget, commands []commandInfo, tools []string) []asset {
	rendered := map[string]string{
		target.CommandCatalogPath: renderCommandCatalog(commands),
	}

	addAsset := func(path, content string) {
		rendered[path] = content
	}

	for _, tool := range tools {
		switch target.Scope {
		case ScopeGlobal:
			switch tool {
			case ToolCodex:
				addAsset(globalCodexSkillRelPath, renderCodexSkill(relativeReference(globalCodexSkillRelPath, target.CommandCatalogPath)))
			case ToolClaude:
				addAsset(globalClaudeInspectRelPath, renderClaudeCommand("Inspect Tailscale control-plane state with tscli.", relativeReference(globalClaudeInspectRelPath, target.CommandCatalogPath), true))
				addAsset(globalClaudeOperateRelPath, renderClaudeCommand("Operate Tailscale resources with tscli.", relativeReference(globalClaudeOperateRelPath, target.CommandCatalogPath), false))
			case ToolOpenCode:
				addAsset(globalOpenCodeInspectRelPath, renderOpenCodeCommand("Inspect Tailscale control-plane state with tscli.", relativeReference(globalOpenCodeInspectRelPath, target.CommandCatalogPath), true))
				addAsset(globalOpenCodeOperateRelPath, renderOpenCodeCommand("Operate Tailscale resources with tscli.", relativeReference(globalOpenCodeOperateRelPath, target.CommandCatalogPath), false))
			}
		default:
			switch tool {
			case ToolGeneric, ToolOpenCode:
				addAsset(localAgentsRelPath, renderAgentsFile(target, tools, len(commands)))
			case ToolCodex:
				addAsset(localCodexSkillRelPath, renderCodexSkill(relativeReference(localCodexSkillRelPath, target.CommandCatalogPath)))
			case ToolClaude:
				addAsset(localClaudeRelPath, renderClaudeProjectFile(relativeReference(localClaudeRelPath, target.CommandCatalogPath)))
			case ToolCopilot:
				addAsset(localGitHubSkillRelPath, renderGitHubSkill(relativeReference(localGitHubSkillRelPath, target.CommandCatalogPath)))
				addAsset(localGitHubInspectRelPath, renderInspectPrompt(relativeReference(localGitHubInspectRelPath, target.CommandCatalogPath)))
				addAsset(localGitHubOperateRelPath, renderOperatePrompt(relativeReference(localGitHubOperateRelPath, target.CommandCatalogPath)))
			}
		}
	}

	assets := make([]asset, 0, len(rendered))
	for path, content := range rendered {
		assets = append(assets, asset{Path: path, Content: content})
	}
	slices.SortFunc(assets, func(a, b asset) int {
		return strings.Compare(a.Path, b.Path)
	})
	return assets
}

