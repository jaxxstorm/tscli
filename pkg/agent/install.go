func renderCommandCatalog(commands []commandInfo) string {
	var b strings.Builder
	b.WriteString("<!-- Code generated by `tscli agent init`; DO NOT EDIT. -->\n\n")
	b.WriteString("# tscli Agent Command Catalog\n\n")
	b.WriteString("Use this generated inventory to map tasks onto the current `tscli` command tree. When flags are unclear, follow up with `tscli <leaf-command> --help`.\n\n")

	currentGroup := ""
	for _, command := range commands {
		if command.Group != currentGroup {
			if currentGroup != "" {
				b.WriteString("\n")
			}
			currentGroup = command.Group
			b.WriteString(fmt.Sprintf("## `%s`\n\n", currentGroup))
		}
		if command.Short == "" {
			b.WriteString(fmt.Sprintf("- `%s`\n", command.Path))
			continue
		}
		b.WriteString(fmt.Sprintf("- `%s`: %s\n", command.Path, command.Short))
	}
	return b.String()
}

