package main

import (
	"bytes"
	"errors"
	"flag"
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"slices"
	"strings"

	"github.com/jaxxstorm/tscli/internal/cli"
	"github.com/spf13/cobra"
	cobraDoc "github.com/spf13/cobra/doc"
)

const (
	defaultOutDir = "docs/commands"
)

var errDocsOutOfDate = errors.New("generated docs are stale or missing")

type generator struct {
	outDir string
}

func main() {
	mode := flag.String("mode", "generate", "Generation mode: generate or check")
	outDir := flag.String("out", defaultOutDir, "Output directory for generated command docs")
	flag.Parse()

	g := generator{outDir: *outDir}

	var err error
	switch strings.ToLower(strings.TrimSpace(*mode)) {
	case "generate":
		err = g.generate()
	case "check":
		err = g.check()
	default:
		err = fmt.Errorf("invalid mode %q: expected generate or check", *mode)
	}

	if err != nil {
		fatalf("%v", err)
	}
}

func (g generator) generate() error {
	renderedDir, cleanup, err := renderToTempDir()
	if err != nil {
		return err
	}
	defer cleanup()

	if err := os.RemoveAll(g.outDir); err != nil {
		return fmt.Errorf("remove output directory %s: %w", g.outDir, err)
	}
	if err := copyTree(renderedDir, g.outDir); err != nil {
		return fmt.Errorf("write generated docs: %w", err)
	}

	fmt.Printf("generated command docs in %s\n", filepath.Clean(g.outDir))
	return nil
}

func (g generator) check() error {
	renderedDir, cleanup, err := renderToTempDir()
	if err != nil {
		return err
	}
	defer cleanup()

	diffs, err := diffDirectories(g.outDir, renderedDir)
	if err != nil {
		return err
	}
	if len(diffs) == 0 {
		fmt.Printf("command docs are up to date in %s\n", filepath.Clean(g.outDir))
		return nil
	}

	fmt.Fprintf(os.Stderr, "command docs check failed (%d differences):\n", len(diffs))
	for _, diff := range diffs {
		fmt.Fprintf(os.Stderr, "- %s\n", diff)
	}
	fmt.Fprintln(os.Stderr, "run `make docs-generate` and commit the updated docs")
	return errDocsOutOfDate
}

func renderToTempDir() (string, func(), error) {
	root := cli.Configure()
	prepareCommandTree(root)

	tmpDir, err := os.MkdirTemp("", "tscli-docsgen-*")
	if err != nil {
		return "", nil, fmt.Errorf("create temp directory: %w", err)
	}

	cleanup := func() { _ = os.RemoveAll(tmpDir) }

	prepender := func(string) string {
		return "<!-- Code generated by `make docs-generate`; DO NOT EDIT. -->\n\n"
	}
	linkHandler := func(name string) string {
		return name
	}

	if err := cobraDoc.GenMarkdownTreeCustom(root, tmpDir, prepender, linkHandler); err != nil {
		cleanup()
		return "", nil, fmt.Errorf("generate command docs: %w", err)
	}
	if err := writeCommandsIndexFiles(tmpDir, root); err != nil {
		cleanup()
		return "", nil, fmt.Errorf("write command index docs: %w", err)
	}

	return tmpDir, cleanup, nil
}

func prepareCommandTree(root *cobra.Command) {
	walk := func(cmd *cobra.Command) {}
	walk = func(cmd *cobra.Command) {
		cmd.DisableAutoGenTag = true

		children := append([]*cobra.Command(nil), cmd.Commands()...)
		for _, child := range children {
			if shouldExcludeCommand(child) {
				cmd.RemoveCommand(child)
				continue
			}
			walk(child)
		}
	}

	walk(root)
}

func shouldExcludeCommand(cmd *cobra.Command) bool {
	if cmd.Hidden {
		return true
	}
	switch cmd.Name() {
	case "help", "completion":
		return true
	default:
		return false
	}
}

func writeCommandsIndexFiles(outDir string, root *cobra.Command) error {
	paths := commandPaths(root)
	if len(paths) == 0 {
		return fmt.Errorf("no command paths discovered")
	}

	var readme strings.Builder
	readme.WriteString("<!-- Code generated by `make docs-generate`; DO NOT EDIT. -->\n\n")
	readme.WriteString("# Command Reference\n\n")
	readme.WriteString("This directory contains command reference pages generated from the current Cobra command tree.\n\n")
	readme.WriteString("Regenerate with:\n\n")
	readme.WriteString("```bash\n")
	readme.WriteString("make docs-generate\n")
	readme.WriteString("```\n\n")
	readme.WriteString("## Commands\n\n")

	var sidebar strings.Builder
	sidebar.WriteString("<!-- Code generated by `make docs-generate`; DO NOT EDIT. -->\n\n")
	for _, path := range paths {
		label := path
		file := filenameForCommandPath(path)
		readme.WriteString(fmt.Sprintf("- [%s](%s)\n", label, file))
		sidebar.WriteString(fmt.Sprintf("- [%s](%s)\n", label, file))
	}

	if err := os.WriteFile(filepath.Join(outDir, "README.md"), []byte(readme.String()), 0o644); err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(outDir, "_sidebar.md"), []byte(sidebar.String()), 0o644); err != nil {
		return err
	}

	return nil
}

func commandPaths(root *cobra.Command) []string {
	paths := []string{}

	var walk func(*cobra.Command)
	walk = func(cmd *cobra.Command) {
		if cmd.CommandPath() != "" {
			paths = append(paths, cmd.CommandPath())
		}

		children := append([]*cobra.Command(nil), cmd.Commands()...)
		slices.SortFunc(children, func(a, b *cobra.Command) int {
			return strings.Compare(a.Name(), b.Name())
		})

		for _, child := range children {
			if shouldExcludeCommand(child) {
				continue
			}
			walk(child)
		}
	}

	walk(root)
	slices.Sort(paths)
	return paths
}

func filenameForCommandPath(path string) string {
	return strings.ReplaceAll(path, " ", "_") + ".md"
}

func diffDirectories(actualDir string, expectedDir string) ([]string, error) {
	actual, err := loadFiles(actualDir)
	if err != nil {
		if !errors.Is(err, os.ErrNotExist) {
			return nil, fmt.Errorf("load actual docs from %s: %w", actualDir, err)
		}
		actual = map[string][]byte{}
	}
	expected, err := loadFiles(expectedDir)
	if err != nil {
		return nil, fmt.Errorf("load expected docs from %s: %w", expectedDir, err)
	}

	diffs := make([]string, 0)
	for path, expectedContent := range expected {
		actualContent, exists := actual[path]
		if !exists {
			diffs = append(diffs, fmt.Sprintf("missing file: %s", path))
			continue
		}
		if !bytes.Equal(actualContent, expectedContent) {
			diffs = append(diffs, fmt.Sprintf("content differs: %s", path))
		}
	}

	for path := range actual {
		if _, exists := expected[path]; !exists {
			diffs = append(diffs, fmt.Sprintf("unexpected file: %s", path))
		}
	}

	slices.Sort(diffs)
	return diffs, nil
}

func loadFiles(root string) (map[string][]byte, error) {
	files := map[string][]byte{}

	err := filepath.WalkDir(root, func(path string, d fs.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}
		if d.IsDir() {
			return nil
		}

		rel, err := filepath.Rel(root, path)
		if err != nil {
			return err
		}
		rel = filepath.ToSlash(rel)

		content, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		files[rel] = content
		return nil
	})

	if err != nil {
		return nil, err
	}

	return files, nil
}

func copyTree(from string, to string) error {
	if err := os.MkdirAll(to, 0o755); err != nil {
		return err
	}

	return filepath.WalkDir(from, func(path string, d fs.DirEntry, walkErr error) error {
		if walkErr != nil {
			return walkErr
		}

		rel, err := filepath.Rel(from, path)
		if err != nil {
			return err
		}
		if rel == "." {
			return nil
		}

		target := filepath.Join(to, rel)
		if d.IsDir() {
			return os.MkdirAll(target, 0o755)
		}

		b, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		return os.WriteFile(target, b, 0o644)
	})
}

func fatalf(format string, args ...any) {
	_, _ = fmt.Fprintf(os.Stderr, format+"\n", args...)
	os.Exit(1)
}
